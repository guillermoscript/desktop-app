import * as yup from "yup";

export const polizaSchema = () => yup
    .object({
        insuranceCarrierId: yup.number().required("Seleccione una Aseguradora"),
        policyNum: yup.number().required("El numero de poliza es requerido"),
        policyStatusId: yup.number().required("Selecciones un Estado de Poliza"),
        insuredValue: yup.number().required("El valor asegurado es requerido"),
        // branchTypeId: yup.number().required("Seleccione un Tipo de ramo"),
        subBranchId: yup.number().required("Seleccione un Subramo"),
        Risk: yup.string("el nombre del riesgo es requerido"),
        Renovable: yup.boolean().required("El campo Renovable es requerido"),
        clientId: yup.number().required("Seleccione un Cliente"),
        agentId: yup.number().required("Seleccione un Agente"),
        relationId: yup.number().required("Seleccione una Relacion"),
        periodicityId: yup.number().required("Seleccione una Periodicidad"),
        currencyId: yup.number().required("Seleccione una Moneda"),
        primeValue: yup.number().required("El valor de la prima es requerido"),
        AnnexValue: yup.number().required("El valor del anexo es requerido"),
        comission: yup.number().required("El valor de la comision es requerido"),
        comissionPolicyStatus: yup.boolean().required("El campo Comision es requerido"),
        ValorFinalizacion: yup.number().required("El valor de finalizacion es requerido"),
        Total: yup.number().required("El valor total es requerido"),
        periodStartDate: yup.date().required("La fecha de inicio de poliza es requerida"),
        periodEndDate: yup.date().required("La fecha de fin de poliza es requerida"),
        renewal: yup.number().required("El numero de renovaciones es requerido"),
        type: yup.string(),
        totalValue: yup.number(),
        machineryValue: yup.number(),
        furnitureValue: yup.number(),
        startCountry: yup.string(),
        endCountry: yup.string(),
        startDate: yup.date(),
        endDate: yup.date(),
        brand: yup.string(),
        class: yup.string(),
        model: yup.string(),
        vehicleType: yup.string(),
        serviceType: yup.string(),
        gasConverted: yup.boolean(),
        vehicleAge: yup.number(),

    })
    .required();
